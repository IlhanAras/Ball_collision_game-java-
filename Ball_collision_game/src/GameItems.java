public class GameItems {
    private String colorName;

    private int point=0;
    public GameItems() {

    }

    public String getColorName() {
        return colorName;
    }


    public int getPoint() {
        return point;
    }

    public void setColorName(String colorName) {
        this.colorName = colorName;
    }


    public void setPoint(int point) {
        this.point = point;
    }
}
